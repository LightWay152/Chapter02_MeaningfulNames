package rules.goodnaming;

public class Rule02_AvoidDisinformation {
	public static String nameOfStudentAInClass = "Tran Van Nam";
	public static String nameOfStudentBInClass = "Nguyen Thi Met";
	
	public static void main(String[] args) {
		System.out.println("Name of student A in class is: "+nameOfStudentAInClass);
		System.out.println("Name of student B in class is: "+nameOfStudentBInClass);
	}

}
